import UIKit

extension UIColor {
    
    class var veryLightBlue: UIColor {
        return UIColor(red: 228.0 / 255.0, green: 232.0 / 255.0, blue: 240.0 / 255.0, alpha: 1.0)
    }
    
    class var blueGray: UIColor {
        return UIColor(red: 127.0 / 255.0, green: 135.0 / 255.0, blue: 149.0 / 255.0, alpha: 1.0)
    }
    
    class var lightishBlue: UIColor {
        return UIColor(red: 60.0 / 255.0, green: 92.0 / 255.0, blue: 1.0, alpha: 1.0)
    }
    
    class var translucentlack: UIColor {
        return UIColor(white: 0.0, alpha: 0.55)
    }
}
